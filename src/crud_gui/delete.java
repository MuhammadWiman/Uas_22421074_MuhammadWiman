    private void tampilTabel() {
        try {
            String[] columnNames = {"ID", "Kode Buku", "Tanggal Peminjaman","Tanggal Pengembalian"};
            ResultSet rs = st.executeQuery("SELECT * FROM peminjaman WHERE username='"+login.txt_username.getText()+"'");
            DefaultTableModel tb= new DefaultTableModel(columnNames,0);
            while (rs.next()) {
                String id = rs.getString("id_pinjam");
                String kode_buku = rs.getString("kode_buku");
                String tanggal_peminjaman = String.valueOf(rs.getDate("tanggal_peminjaman"));
                String tanggal_pengembalian =String.valueOf(rs.getDate("tanggal_pengembalian"));
                String[] data = {id,kode_buku,tanggal_peminjaman,tanggal_pengembalian};
                tb.addRow(data);
            }
            jt.setModel(tb);
        }
        catch (SQLException e) {
            
        }
    }
