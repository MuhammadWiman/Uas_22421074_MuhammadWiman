    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        String usrID =jt.getValueAt(jt.getSelectedRow(),0).toString();
        try {
            PreparedStatement pStatement = null;
            String sql ="DELETE FROM peminjaman WHERE id_pinjam=? ";
            pStatement = conn.prepareStatement(sql);
            pStatement.setString(1, usrID);
            int intTambah= pStatement.executeUpdate();
            if (intTambah>0)
                JOptionPane.showMessageDialog(this,
                    "Hapus data sukses", "Informasi",
                    JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(this,
                    "Hapus data gagal", "Informasi",
                    JOptionPane.INFORMATION_MESSAGE);
            pStatement.close();
            tampilTabel();
        }
        catch (SQLException e) {
            System.out.println("koneksi gagal " + e.toString());
        }  
    }//GEN-LAST:event_btn_hapusActionPerformed

